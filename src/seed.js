export async function seedFirestore() {
  console.log('🌱 Cargando datos reales de Academia LR...')
  let ok = 0, err = 0
  for (const student of STUDENTS) {
    const { asistencia, pagos, ...base } = student
    try {
      await setDoc(doc(db, 'alumnos', student.id), base)
      for (const { f, m } of asistencia) {
        const key = f.replace('/', '-')
        await setDoc(doc(db, 'alumnos', student.id, 'asistencia', key), { fecha: f, marca: m })
      }
      for (const [mes, monto] of Object.entries(pagos)) {
        await setDoc(doc(db, 'alumnos', student.id, 'pagos', mes), { mes, monto })
      }
      ok++
      console.log('  ✅', base.nombre)
    } catch(e) {
      err++
      console.error('  ❌', base.nombre, e.message)
    }
  }
  console.log(`🎾 Completado: ${ok} alumnos cargados, ${err} errores`)
}
